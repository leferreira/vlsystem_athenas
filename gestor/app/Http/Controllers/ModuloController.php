<?php

namespace App\Http\Controllers;

use App\Models\Modulo;
use Illuminate\Http\Request;

class ModuloController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $dados["lista"] = Modulo::all();
        return view("Modulo.Index", $dados);
    }

    
    public function create()
    {
        $dados = array();
        return view("Modulo.Create", $dados);
    }

    
    public function store(Request $request)
    {
       
        $req = $request->except(["_token","_method"]);
        Modulo::firstOrCreate($req);
        return redirect()->route('modulo.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $dados["modulo"] = Modulo::find($id);
        $dados["lista"] = Modulo::all();
        return view('Modulo.Index', $dados);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $req     =   $request->except(["_token","_method"]);
        Modulo::where("id", $id)->update($req);
        return redirect()->route("modulo.index");
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try{
            $h = Modulo::find($id);
            $h->delete();
            return redirect()->back()->with('msg_sucesso', "Ãtem apagado com sucesso.");
        }catch (\Exception $e){
            $cod = $e->getCode();
            return redirect()->back()->with('msg_erro', "Houve um problema ao apagar [$cod]");
        }
    }
}
